#!/usr/bin/env node
/**
 * Generates docs/toolpacks-index.md from built dist (run after `pnpm build`).
 * Usage: node scripts/generate-toolpack-index.mjs [--check]
 */
import { readFileSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath, pathToFileURL } from "node:url";

const root = join(dirname(fileURLToPath(import.meta.url)), "..");
const outPath = join(root, "docs", "toolpacks-index.md");

const categoryLabels = {
  "context-engineering": "Context Engineering",
  analysis: "Analysis",
  "engineering-support": "Engineering Support",
  "init-enhancement": "Initialization Enhancement",
};

const mod = await import(pathToFileURL(join(root, "dist", "index.js")).href);
const { getOptionalToolpacks } = mod;

const packs = getOptionalToolpacks(root);
const lines = [
  "# Toolpack index",
  "",
  "> Auto-generated by `pnpm run generate:toolpack-index`. Do not edit by hand.",
  "",
];

let lastCategory = "";
for (const pack of packs) {
  if (pack.category !== lastCategory) {
    if (lastCategory) lines.push("");
    lines.push(`## ${categoryLabels[pack.category] ?? pack.category}`);
    lines.push("");
    lastCategory = pack.category;
  }
  lines.push(`- **${pack.id}** — ${pack.name}`);
  lines.push(`  - ${pack.description}`);
  lines.push(`  - source=\`${pack.packSource}\` version=\`${pack.packVersion}\``);
  lines.push("");
}

const content = lines.join("\n").trimEnd() + "\n";

const check = process.argv.includes("--check");
if (check) {
  const existing = readFileSync(outPath, "utf-8");
  if (existing !== content) {
    console.error("toolpacks-index.md is out of date. Run: pnpm run generate:toolpack-index");
    process.exit(1);
  }
  console.log("toolpacks-index.md is up to date.");
} else {
  writeFileSync(outPath, content, "utf-8");
  console.log(`Wrote ${outPath}`);
}
